                duration: 1,
                width: '100%',
                height: '80vh',
                borderRadius: '0px',
                ease: "power1.inOut",
                stagger: 1,
            });

            ScrollTrigger.create({
                trigger: ".product-banner-img",
                start: "top top",
                end: "bottom bottom",
                pin: ".product-img-container",
                animation: animation,
                scrub: true,
                // markers: true,
                threshold: 1,
            });


        }
    })
