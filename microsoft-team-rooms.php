    const bannerTimeline = gsap.timeline({
        scrollTrigger: {
            trigger: "#main",
            start: "top top",
            end: "center center",
            // pin: '#horizontal-scoll',
            scrub: true,
            markers: false,
            invalidateOnRefresh: true,
        },
    });

    if (window.innerWidth > 767) {
        bannerTimeline.to(
            ".first-img", {
                x: () => -1000,
            },
            "<"
        );
        bannerTimeline.to(
            ".second-img", {
                x: () => 1000,
            },
            "<"
        );

    } else {
        bannerTimeline.to(
            ".first-img", {
                y: () => -1000,
            },
            "<"
        );
        bannerTimeline.to(
            ".second-img", {
                y: () => 1000,
            },
            "<"
        );
    }


    let bannerSection = document.querySelector('.brand-card');

    gsap.to('.brand-card', {
        x: () => bannerSection.scrollWidth * -1,
        xPercent: 100,
        scrollTrigger: {
            trigger: '.brand-card',
            start: 'center-=30px center',
            end: '+=1300px',
            pin: '#brand-banner',
            scrub: true,
            // markers:true,
            invalidateOnRefresh: true,
        }
    });






    // ------------------------------------devices--------------------------------
    if (window.innerWidth > 768) {
        gsap.set(".device-photo:not(:first-child)", {
            x: "0%",
            opacity: 0
        });


        const deviceAnimation = gsap.to(".device-photo:not(:first-child)", {
            x: 0,
            opacity: 1,
            duration: 1,
            stagger: 1,
        });

        ScrollTrigger.create({
            trigger: ".device-details-wrapper",
            start: "top top",
            end: "bottom bottom",
            pin: ".device-right",
            animation: deviceAnimation,
            scrub: true,
            markers: false,
            threshold: 1,
        });



        const images = document.querySelectorAll('.device-details');

        images.forEach((img, index) => {
            gsap.set(img, {
                x: "0%",
                opacity: 0,
            });

            const simulatedAfter = gsap.to(img, {
                x: 0,
                opacity: 1,
                duration: 0.2,
                stagger: 1,
                scrollTrigger: {
                    trigger: img,
                    start: 'top center',
                    end: 'bottom center',
                    //   markers: true,
                }
            });

            ScrollTrigger.create({
                trigger: img,
                start: 'top center',
                end: 'center center',
                onEnter: () => {
                    simulatedAfter.play();
                },
                onLeaveBack: () => {
                    simulatedAfter.reverse();
                },
            });
        });
    }
